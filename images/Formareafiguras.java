    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        libreriareasfiguras objeto1 = new libreriareasfiguras();
        
        Integer area = 0;
        
       // quitar();
        if(jRadioButton1.isSelected()){
            Integer a= Integer.valueOf(jTextField2.getText());
            Integer b= Integer.valueOf(jTextField3.getText());
            area = objeto1.Areatriangulorectangulo(a, b);
            jLabel12.setText(String.valueOf(area));
        }
        else  if(jRadioButton2.isSelected()){
            Integer a= Integer.valueOf(jTextField2.getText());
            area = objeto1.Areacuadrado(a);
            jLabel12.setText(String.valueOf(area));
            
        }
        else  if(jRadioButton3.isSelected()){
            Integer a= Integer.valueOf(jTextField2.getText());
            Integer b= Integer.valueOf(jTextField3.getText());
            area = objeto1.Arearectangulo(a, b);
            jLabel12.setText(String.valueOf(area));
        }
        else  if(jRadioButton4.isSelected()){
             Integer a= Integer.valueOf(jTextField4.getText());
            Integer b= Integer.valueOf(jTextField5.getText());
            area = objeto1.Arearombo(a, b);
            jLabel12.setText(String.valueOf(area));
            
        }
        else  if(jRadioButton5.isSelected()){
            Integer a= Integer.valueOf(jTextField2.getText());
            Integer b= Integer.valueOf(jTextField3.getText());
            area = objeto1.Arearomboide(a, b);
            jLabel12.setText(String.valueOf(area));
            
        }
        else  if(jRadioButton6.isSelected()){
            Integer a=Integer.valueOf(jTextField3.getText());
            Integer b=Integer.valueOf(jTextField6.getText());
            Integer c=Integer.valueOf(jTextField7.getText());
            area = objeto1.Areatrapecio(a, b, c);
            jLabel12.setText(String.valueOf(area));
            
        }
     
    }//GEN-LAST:event_jButton2MouseClicked

