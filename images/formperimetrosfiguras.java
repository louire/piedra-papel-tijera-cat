    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        libreriaperimetrofiguras objeto1= new libreriaperimetrofiguras();
        Integer perimetro=0;
        if(jRadioButton1.isSelected()){
            Integer a= Integer.valueOf(jTextField1lado1.getText());
            Integer b= Integer.valueOf(jTextField2lado2.getText());
            Integer c= Integer.valueOf(jTextField3lado3.getText());
            perimetro = objeto1.Perimetrotriangulorectangulo(a, b, c);
            jLabel6.setText(String.valueOf(perimetro));
        }
        else  if(jRadioButton2.isSelected()){
            Integer a= Integer.valueOf(jTextField1lado1.getText());
            perimetro = objeto1.Perimetrocuadrado(a);
            jLabel6.setText(String.valueOf(perimetro));
            
        }
        else  if(jRadioButton3.isSelected()){
            Integer a= Integer.valueOf(jTextField5base.getText());
            Integer b= Integer.valueOf(jTextField6altura.getText());
            perimetro = objeto1.Perimetrorectangulo(a, b);
            jLabel6.setText(String.valueOf(perimetro));
        }
        else  if(jRadioButton4.isSelected()){
             Integer a= Integer.valueOf(jTextField1lado1.getText());
            perimetro = objeto1.Perimetrorombo(a);
            jLabel6.setText(String.valueOf(perimetro));
            
        }
        else  if(jRadioButton5.isSelected()){
            Integer a= Integer.valueOf(jTextField5base.getText());
            Integer b= Integer.valueOf(jTextField6altura.getText());
            perimetro= objeto1.Perimetroromboide(a, b);
            jLabel6.setText(String.valueOf(perimetro));
            
        }
        else  if(jRadioButton6.isSelected()){
            Integer a= Integer.valueOf(jTextField1lado1.getText());
            Integer b= Integer.valueOf(jTextField2lado2.getText());
            Integer c= Integer.valueOf(jTextField3lado3.getText());
            Integer d= Integer.valueOf(jTextField4lado4.getText());
            perimetro = objeto1.Perimetrotrapecio(a, b, c, d);
            jLabel6.setText(String.valueOf(perimetro));   
        }     
    }//GEN-LAST:event_jButton1MouseClicked

